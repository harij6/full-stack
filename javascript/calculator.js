function add(a, b) {
    return a + b;
}
function subtract(a, b) {
    return a - b;
}
function multiply(a, b) {
    return a * b;
}
function divide(a, b) {
    return a / b;
}
console.log("Add:", add(10, 5));
console.log("Subtract:", subtract(25, 5));
console.log("Multiply:", multiply(15, 5));
console.log("Divide:", divide(0, 5));